// Counter animation
function animateCounter(element, finalValue) {
    const duration = 2000; // 2 seconds
    const start = performance.now();
    
    // Extract number from string (e.g., "17,000+" -> 17000)
    const numericValue = parseInt(finalValue.replace(/[^\d]/g, '')) || 0;
    const suffix = finalValue.replace(/[\d,]/g, '');
    
    function updateCounter(currentTime) {
        const elapsed = currentTime - start;
        const progress = Math.min(elapsed / duration, 1);
        
        // Easing function for smooth animation
        const easeOutCubic = 1 - Math.pow(1 - progress, 3);
        const currentValue = Math.floor(numericValue * easeOutCubic);
        
        // Format number with commas if needed
        let displayValue = currentValue.toLocaleString();
        if (finalValue.includes(',')) {
            displayValue = displayValue;
        } else {
            displayValue = currentValue.toString();
        }
        
        element.textContent = displayValue + suffix;
        
        if (progress < 1) {
            requestAnimationFrame(updateCounter);
        } else {
            element.textContent = finalValue; // Ensure final value is exact
        }
    }
    
    requestAnimationFrame(updateCounter);
}

// Parallax effect for hero section
window.addEventListener('scroll', function() {
    const scrolled = window.pageYOffset;
    const hero = document.querySelector('.hero');
    const heroHeight = hero.offsetHeight;
    
    if (scrolled < heroHeight) {
        const rate = scrolled * -0.5;
        hero.style.transform = `translateY(${rate}px)`;
    }
});

